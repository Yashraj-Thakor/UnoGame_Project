public abstract class UnoCard extends Card {
    
    public enum Color {
        RED, BLUE, GREEN, YELLOW, WILD
    }
    
    private Color color;

    public UnoCard(Color color) {
        this.color = color;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }
}